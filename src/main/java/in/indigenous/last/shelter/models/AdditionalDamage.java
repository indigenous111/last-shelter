package in.indigenous.last.shelter.models;

public class AdditionalDamage {

	private double damage;

	private int turns;

	public double getDamage() {
		return damage;
	}

	public void setDamage(double damage) {
		this.damage = damage;
	}

	public int getTurns() {
		return turns;
	}

	public void setTurns(int turns) {
		this.turns = turns;
	}

}
